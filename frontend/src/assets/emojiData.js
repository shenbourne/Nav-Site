/**
 * Build a shortcode -> emoji lookup map for quick resolution.
 */
export const shortcodeMap = {}
for (const cat of emojiCategories) {
  for (const item of cat.emojis) {
    shortcodeMap[item.code] = item.emoji
  }
}

/**
 * Resolve a shortcode string (e.g. ":smile:") to its emoji character.
 * Returns the original string if no match found.
 */
export function resolveShortcode(input) {
  if (!input) return input
  const trimmed = input.trim()
  if (shortcodeMap[trimmed]) return shortcodeMap[trimmed]
  // Try wrapping with colons if not already
  if (!trimmed.startsWith(':')) {
    const wrapped = `:${trimmed}:`
    if (shortcodeMap[wrapped]) return shortcodeMap[wrapped]
  }
  return input
}
